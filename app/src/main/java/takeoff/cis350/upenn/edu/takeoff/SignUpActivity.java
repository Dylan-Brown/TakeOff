    // Attempts to sign up the  user
    private void attemptSignUp() {

        // Reset errors.
        mEmailView.setError(null);
        mPasswordView.setError(null);

        // Store values at the time of the login attempt.
        String email = mEmailView.getText().toString();
        String password = mPasswordView.getText().toString();
        String password2 = mPasswordConfirmView.getText().toString();
        System.out.println("Email entered: " + email);
        System.out.println("Password entered: " + password);
        System.out.println("Password2 entered: " + password2);

        boolean cancel = false;
        View focusView = null;

        // Check for a valid password, if the user entered one.
        if (!TextUtils.isEmpty(password) && password.length() < 4) {
            mPasswordView.setError("Password must be at least 4 characters");
            focusView = mPasswordView;
            cancel = true;
        }

        // Check for a valid email address.
        if (TextUtils.isEmpty(email)) {
            mEmailView.setError(getString(R.string.error_field_required));
            focusView = mEmailView;
            cancel = true;
        } else if (!email.contains("@")) {
            mEmailView.setError(getString(R.string.error_invalid_email));
            focusView = mEmailView;
            cancel = true;
        }

        if (cancel) {
            // There was an error; don't attempt login and focus the first
            // form field with an error.
            focusView.requestFocus();
        } else {
            // Show a progress spinner, and kick off a background task to
            // perform the user login attempt.
            if (database.isUser(email)) {
                Context context = getApplicationContext();
                CharSequence text = "That username is already in use.";
                int duration = Toast.LENGTH_SHORT;

                Toast toast = Toast.makeText(context, text, duration);
                toast.show();
            } else {
                if (password.equals(password2)) {
                    database.addUser(email, password);
                    loggedInUser = database.getUser(email);
                    Intent intent = new  Intent(this, SearchInfo.class);
                    //start Activity intent
                    startActivity(intent);
                } else {
                    Context context = getApplicationContext();
                    CharSequence text = "Passwords do not match.";
                    int duration = Toast.LENGTH_SHORT;

                    Toast toast = Toast.makeText(context, text, duration);
                    toast.show();
                }
            }
        }
    }

